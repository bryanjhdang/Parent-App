package cmpt276.as3.cmpt276hydrogenproject.model;

import androidx.annotation.NonNull;

public class Child {
    String name;

    public Child(String name) {
        this.name = name;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    @NonNull
    @Override
    public String toString() {
        return name;
    }
}
